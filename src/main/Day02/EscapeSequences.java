package Day02;

public class EscapeSequences {

    public static void main (String[] args){
        System.out.println("\thello helooo");
        System.out.println("\t\t\t\thello");
        System.out.println("\t\thelo\t\n\theo\tkooo");
        // \t gives space
        // \n for next line
        // \"
        System.out.println("//");
        System.out.println("today is \" friday\"");
        System.out.println("today is \rmonday\r ");
        System.out.println("today is\tTuesday\t " );
        System.out.println("today is\s\s\s\s\sWednsday\s "  );
        System.out.println("today is \fthursday\f ");
        System.out.println("today is \bsaturday\b");
        System.out.println("today is \nToday\n");
        System.out.println("Today is \fTomorow\f");
        System.out.println("Today is \bdoor\b");
        System.out.println("Today is \table\t");
        System.out.println("Today is \'koru\' ");



    }
}
